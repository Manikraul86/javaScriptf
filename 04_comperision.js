/************Comperision ***********/
// == Comperision with == and ===
// == Comperision with == will check only value
// === Comperision with === will check value and type   
let a = 5;
let b = "5";    
console.log(a == b); // true
console.log(a === b); // false  
// != Comperision with != will check only value
// !== Comperision with !== will check value and type
console.log(a != b); // false
console.log(a !== b); // true


