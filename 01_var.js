// Variables are containers for storing data values. In JavaScript, you can declare variables using the var, let, or const keywords. Each of these keywords has different characteristics and use cases.

// The var keyword is function-scoped and can be redeclared and updated. However, it is generally recommended to avoid using var due to its quirks and potential for bugs.

// Example:

var userName = "Manik Raul";
console.log(userName);


//Const: The const keyword is block-scoped and cannot be updated or redeclared. It is used for variables that should remain constant throughout the program.

// Here is an example of using let and const:

const userId = 123
const userName = "Coder"

// Let: The let keyword is block-scoped and can be updated but not redeclared within the same scope. It is commonly used for variables that may change over time.
// Here is an example of using let and const:

let userEmail ="coder@gmail.com";
